public class IntergerLiteralDemo {

    public static void main(String[] args) {
        int data = 0b1001;
        System.out.println("Data >>> " + data);
        data = 123_456;
        System.out.println("Data >> " + data);
        data = 0XAABB11;
        System.out.println("Data >>> " + data);
        data = 017;
        System.out.println("Data >>> " + data);
    }

}
